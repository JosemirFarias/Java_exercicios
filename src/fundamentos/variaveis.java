package fundamentos;

public class variaveis {

    public static void main(String[] args) {

        // Declaração de variáveis
        int anoAtual = 2024;
        int anoNascimento = 1989;

        // Cálculo da idade
        int idade = anoAtual - anoNascimento;

        // Exibição da idade
        System.out.println("Josemir tem " + idade + " anos de idade.");
    }
    
}
